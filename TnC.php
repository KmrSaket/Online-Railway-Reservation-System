    <h1>TERMS AND CONDITION</h1>

    <ul>
          <li>Registration as an agent is issued for booking of E- Tickets only</li>
          <li>All rights to enable VLEs for ORRS service remains with CSC SPV.</li>
          <li>ORRS service will be enabled for VLEs after proper verification, security deposit for
  unsuccessful VLEs will be duly returned</li>
          <li>Agents are required to give the correct address, Mobile No, Mail id & PAN No. in the
  registration form.</li>
          <li>Change of address should be intimated immediately along with the original certificate
  issued, in order to issue a fresh certificate.</li>
          <li>All rules & regulations are to be read properly & followed strictly. Ignorance of the
  same cannot be considered as a valid reason. The agents must be fully aware of the
 extant rules.</li>
          <li>Once received agent code from ORRS on the behalf of CSC e-Governance Service
  India Ltd, my earlier agent code automatically gets deactivated.</li>
          <li>Agents should verify the ids provided to them and also the authorization certificate
  issued. If by mistake, id of another agent is issued to them they should immediately
  inform CSC SPV.</li>
          <li>Agents should keep a sign board outside their agency in which ORRS’s logo can be
  displayed. Sign Board should include: display of rules & regulations, ORRS service
  charges, agent services charges, payment gateway charges etc.</li>
          <li>Certificate of Authorization issued by ORRS should be prominently displayed at the
  agency.</li>
          <li>Agents should practice good business ethics.</li>

    </ul>

			<h1>BOOKING/CANCELLATION OF TICKETS</h1>

      <ul>
        <li>Tickets should be booked only when the customer approaches.</li>
        <li>The agent must ensure that the transaction (Booking or cancellation) is done with the
          complete knowledge and acceptance of the customer.</li>
        <li>It is mandatory for all agents to take the written requests and a copy of the ID proof
         from the customers for both booking & cancellation of the tickets. These requests are
         to be preserved for a period of six months.</li>
        <li>Mobile number of the customer or one of the passenger(s) while booking the ticket is
         mandatory. Mobile number of the passenger(s) must be indicated on ERS.</li>
        <li>The ERS issued by all the agents should be strictly in the format prescribed by ORRS.
         ERS issued by the agent should contain name and full address contact details of the
         same agent and the name of the Principal agent.</li>
        <li>Agents must issue receipt on their own stationary for the amount collected from the
         customers.</li>
        <li>The receipt should contain details like Railway Fare, ORRS’s service charges, agents
         service charges, payment gateway charge etc. (The service Tax as applicable on the
         agents service charges should be shown separately in the receipt).</li>
        <li>Service charges of Agent includes booking as well as cancellation transactions.</li>
      </ul>
